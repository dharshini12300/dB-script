CREATE TABLE employee  
(  
employee_id (10, 0),  
first_Name varchar(25),  
last_Name varchar(25),  
email varchar(25),  
phone_number varchar(25),  
hire_date varchar(25),
jod_id varchar (25),
salary varchar (25),
commission varchar (25),
manager_id varchar (25),
department_id varchar (25)
); 